    // ================== VALIDAÇÕES ==================
    private boolean campoVazio(String valor) {
        return valor == null || valor.trim().isEmpty();
    }

    private boolean emailValido(String email) {
        return Pattern.matches("^[\\w-.]+@([\\w-]+\\.)+[\\w-]{2,4}$", email);
    }

