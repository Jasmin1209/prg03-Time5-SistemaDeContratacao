    public void preencherDados(Long experienciaId) {
        this.experienciaEmEdicao =
        perfilcandidatocontroller.buscarExperienciaPorId(experienciaId);

    if (experienciaEmEdicao == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "ExperiÃªncia nÃ£o encontrada.");
        return;
    }

    txttitulo.setText(experienciaEmEdicao.getCargo());
    txtempresa.setText(experienciaEmEdicao.getEmpresa());

    if (experienciaEmEdicao.getDataInicial() != null) {
        spnDiaInicial.setValue(experienciaEmEdicao.getDataInicial().getDayOfMonth());
        spnMesInicial.setValue(experienciaEmEdicao.getDataInicial().getMonthValue());
        spnAnoInicial.setValue(experienciaEmEdicao.getDataInicial().getYear());
    }

    if (experienciaEmEdicao.getDataFinal() != null) {
        spnDiaFinal.setValue(experienciaEmEdicao.getDataFinal().getDayOfMonth());
        spnMesFinal.setValue(experienciaEmEdicao.getDataFinal().getMonthValue());
        spnAnoFinal.setValue(experienciaEmEdicao.getDataFinal().getYear());
    }
    }

