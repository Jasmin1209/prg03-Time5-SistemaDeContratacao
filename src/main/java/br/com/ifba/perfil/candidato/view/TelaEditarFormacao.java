    private void carregarDados() {
        if (formacao == null) return;


        txtIntituicao.setText(formacao.getInstituicao());
        txtCurso.setText(formacao.getNomeDocurso());
        cmbTipo.setSelectedItem(formacao.getTipo());


        LocalDate inicio = formacao.getDataInicial();
            if (inicio != null) {
                spnDiaInicial.setValue(inicio.getDayOfMonth());
                spnMesInicial.setValue(inicio.getMonthValue());
                spnAnoInicial.setValue(inicio.getYear());
            }


        LocalDate fim = formacao.getDataFinal();
            if (fim != null) {
                spnDiaFinal.setValue(fim.getDayOfMonth());
                spnMesFinal.setValue(fim.getMonthValue());
                spnAnoFinal.setValue(fim.getYear());
            }
    }

