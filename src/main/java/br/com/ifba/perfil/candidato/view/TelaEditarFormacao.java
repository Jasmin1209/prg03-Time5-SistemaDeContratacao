    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        // TODO add your handling code here:
        LocalDate dataInicial = LocalDate.of(
            (int) spnAnoInicial.getValue(),
            (int) spnMesInicial.getValue(),
            (int) spnDiaInicial.getValue()
        );


        LocalDate dataFinal = LocalDate.of(
            (int) spnAnoFinal.getValue(),
            (int) spnMesFinal.getValue(),
            (int) spnDiaFinal.getValue()
        );
        
        if (formacao == null) {
            formacao = new Formacao();
        }


        formacao.setInstituicao(txtIntituicao.getText().trim());
        formacao.setNomeDocurso(txtCurso.getText().trim());
        formacao.setTipo((TipoFormacao)cmbTipo.getSelectedItem());
        formacao.setDataInicial(dataInicial);
        formacao.setDataFinal(dataFinal);


        perfilcandidatocontroller.addFormacao(idPerfil, formacao);


    JOptionPane.showMessageDialog(this, "Formação acadêmica salva com sucesso.");
    dispose();
    }//GEN-LAST:event_btnsalvarActionPerformed

    

